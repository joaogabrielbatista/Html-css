package Model;

public class Dieta {
    private String iddieta;
    private String dt_dieta;
    private String dieta;
    private String aluno_idaluno;
    
    public String getIddieta() {
        return iddieta;
    }
    public void setIddieta(String iddieta) {
        this.iddieta = iddieta;
    }
    public String getDt_dieta() {
        return dt_dieta;
    }
    public void setDt_dieta(String dt_dieta) {
        this.dt_dieta = dt_dieta;
    }
    public String getDieta() {
        return dieta;
    }
    public void setDieta(String dieta) {
        this.dieta = dieta;
    }
    public String getAluno_idaluno() {
        return aluno_idaluno;
    }
    public void setAluno_idaluno(String aluno_idaluno) {
        this.aluno_idaluno = aluno_idaluno;
    }

    
}
